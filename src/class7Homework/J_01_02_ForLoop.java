package class7Homework;

public class J_01_02_ForLoop {
    public static void main(String[] args) {

        // Question 1- Print numbers from 1 to 100 in 1 line with space
        System.out.println("Task- 1");
        for(int i= 1; i<=100; i++){
            System.out.print(i+" ");
        }
        System.out.println();


        // Question2- Print numbers from 100 to 1
        System.out.println("Task-2");
        for(int j=100; j>=1; j--){
            System.out.print(j+" ");
        }

    }
}
