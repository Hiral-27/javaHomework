package class6Homework;

public class J_Task02_WhileLoop_1to100 {
    public static void main(String[] args) {

        // Question 2: Print numbers from 1 to 100 in 1 line with space

        int num=1;
        while(num<=100){
            System.out.print(num);
            System.out.print(" ");
            num++;
        }
    }
}
