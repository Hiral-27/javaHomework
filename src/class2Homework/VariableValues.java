package class2Homework;

public class VariableValues {
    public static void main(String[] args) {
        /*
        TASK 2
        Create a Java program and name it VariableValues
        In your program create variables to store all different types of primitive data.
        Update(reassign) value of each variable
        Print the value of each variable
         */
        byte box1= -125;
        box1= 110;
        System.out.println("Re-assign byte= "+ box1);
        short box2= 5000;
        box2= -23500;
        System.out.println("Re-assign short= "+ box2);
        int box3= 100000;
        box3= -26000;
        System.out.println("Re-assign int= "+ box3);
        long box4= 4567890234l;
        box4= -765433456l;
        System.out.println("Re-assign long= "+ box4);
        float box5= 4566.359f;
        box5= -67.1298f;
        System.out.println("Re-assign float= "+ box5);
        double box6= 956856.74567;
        box6= -345445.56340;
        System.out.println("Re-assign double= "+ box6);
        char symbol= '+';
        symbol= 'T';
        System.out.println("Re-assign char= "+ symbol);
        boolean easy= true;
        easy= false;
        System.out.println("Re-assign boolean= "+ easy);

    }
}
